CREATE OR REPLACE FUNCTION "public"."DocSearch"("query_embedding" "public"."vector", "matchcount" integer, "similaritythreshold" double precision) RETURNS TABLE("chunk_id" "uuid", "section_title" "text", "content" "text", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    "Chunks".chunk_id,
    "Chunks".section_title,
    "Chunks".content,
    ("Chunks".embedding <#> query_embedding) * -1 AS similarity
  FROM "Chunks"
  
  -- Using dot product for similarity check
  WHERE ("Chunks".embedding <#> query_embedding) * -1 > similaritythreshold
  
  -- Order by similarity
  ORDER BY "Chunks".embedding <#> query_embedding
  
  LIMIT matchcount;
END;
$$;

